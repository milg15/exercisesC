short copiarMapa(short nivel) {
	for (short f = 0; f < FILAS; f++) {
		for (short c = 0; c < COLUMNAS; c++) {
			if (nivel == 1) mapaActual[f][c] = mapa_nivel1[f][c];
			else if (nivel == 2) mapaActual[f][c] = mapa_nivel2[f][c];
			else if (nivel == 3) mapaActual[f][c] = mapa_nivel3[f][c];
			else if (nivel == 4) mapaActual[f][c] = mapa_nivel4[f][c];
			else if (nivel == 5) mapaActual[f][c] = mapa_nivel5[f][c];
			else if (nivel == 6) mapaActual[f][c] = mapa_nivel6[f][c];
			else if (nivel == 7) mapaActual[f][c] = mapa_nivel7[f][c];
			else if (nivel == 8) mapaActual[f][c] = mapa_nivel8[f][c];
			else return 0;
		}
	}
	return 1;
}

void mostrarMapa(Personaje* &En) {
	short n = 0;
	for (short f = 0; f < FILAS; f++) {
		for (short c = 0; c < COLUMNAS; c++) {
			short ex = 0, ey = 0;
			pintarMapa(f, c, mapaActual[f][c], ex, ey);
			if (ex != 0 && ey != 0) 
				crearPersonaje(En[n++], ex, ey, 1, 2, 4);
		}
	}
	Console::BackgroundColor = ConsoleColor::Black;
	return;
}

void pintarMapa(short y, short x, short color, short &ex, short &ey) {
	Console::SetCursorPosition(x + 10, y + 2);
	Console::BackgroundColor = ConsoleColor::Black;
	switch (color) {
	case AZUL:  Console::BackgroundColor = ConsoleColor::Blue;
		break;
	case ROJO: Console::BackgroundColor = ConsoleColor::DarkRed;
		break;
	case ENEMIGO: {
		mapaActual[y][x] = 0;
		mapaActual[y+1][x] = 0;
		ex = x + 10;
		ey = y + 2;
		break;
	}
	case PUNTOS: {
		Console::ForegroundColor = ConsoleColor::Magenta;
		cout << char(145);
		return;
	}	
	case VIDAS: {
		Console::ForegroundColor = ConsoleColor::Red;
		cout << char(3);
		return;
	}
	default: return;
	}
	cout << " ";
}

